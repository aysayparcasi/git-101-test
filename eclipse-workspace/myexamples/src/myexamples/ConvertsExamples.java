package myexamples;

public class ConvertsExamples {

	    public static void main(String[] args)
	    {
	 
	        float f = 100.04f;
	 
	       
	        double d = (float)f;
	        System.out.println("Double value " + f);
	        
	        
	        long l = (long)f;
	        System.out.println("Long value " + l);
	         
	        int i = (int)d;
	        System.out.println("Int value " + i);
	 
	       
	   
	 
	    }
}